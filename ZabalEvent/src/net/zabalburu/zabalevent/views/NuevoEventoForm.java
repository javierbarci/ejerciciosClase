    private void mostrar() {
        ImageIcon imIc = new ImageIcon("imagenes/"
                + this.usuario.getFoto());
        Image imag = imIc.getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH);
        imIc.setImage(imag);
        lblFotoUsuario.setIcon(imIc);
        List<Evento> eventos = service.getEventos();
        int idEvento;
        if (eventos.isEmpty()) {
            idEvento = 1;
        } else {
            idEvento = eventos.get(eventos.size() - 1).getIdEvento() + 1;
        }
        lblIdEvento.setText(String.valueOf(idEvento));
        txtTitulo.setText("");
        txaDescripcion.setText("");
        cbxCategoria.setSelectedIndex(0);
        cbxLugar.setSelectedIndex(0);
        ftxFecha.setValue(new Date());
        ftxPrecio.setValue(0.0);

    }

    private void cargarCombos() {
        for (Categoria c : service.getCategorias()) {
            cbxCategoria.addItem(c);
        }
        for (Lugar l : service.getLugares()) {
            cbxLugar.addItem(l);
        }
    }
