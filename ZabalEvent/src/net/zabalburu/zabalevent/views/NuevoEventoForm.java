    private void btnAñadirEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirEventoActionPerformed
        if (txtTitulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe especificarse el título del evento",
                    "Falta Título Evento",
                    JOptionPane.ERROR_MESSAGE);
            txtTitulo.requestFocus();
        } else {
            Evento e = new Evento();
            e.setIdEvento(Integer.parseInt(lblIdEvento.getText()));
            e.setTitulo(txtTitulo.getText());
            e.setDescripcion(txaDescripcion.getText());
            Categoria c = (Categoria) cbxCategoria.getSelectedItem();
            e.setIdCategoria(c.getIdCategoria());
            Lugar l = (Lugar) cbxLugar.getSelectedItem();
            e.setIdLugar(l.getIdLugar());
            e.setIdUsuario(this.usuario.getIdUsuario());
            Date fecha = (Date) ftxFecha.getValue();
            e.setFecha(fecha);
            Number n = (Number) ftxPrecio.getValue();
            e.setPrecio(n.doubleValue());
            e.setFoto(f.getName());
            service.nuevoEvento(e);
            JOptionPane.showMessageDialog(this, "Evento guardado correctamente");
            this.mostrar();
        }
    }//GEN-LAST:event_btnAñadirEventoActionPerformed

    private void lblFotoEventoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblFotoEventoMousePressed
        JFileChooser jfc = new JFileChooser();
        jfc.setMultiSelectionEnabled(false);
        jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileFilter filter = new FileNameExtensionFilter("JPEG (*.jpeg,*.jpg)", "jpg", "jpeg");
        jfc.addChoosableFileFilter(filter);
        filter = new FileNameExtensionFilter("PNG (*.png)", "png");
        jfc.addChoosableFileFilter(filter);
        filter = new FileNameExtensionFilter("GIF (*.gif)", "gif");
        jfc.addChoosableFileFilter(filter);
        filter = new FileNameExtensionFilter("Todas las imágenes", "gif","png","jpg","jpeg","bmp");
        jfc.addChoosableFileFilter(filter);
        jfc.setAcceptAllFileFilterUsed(false);
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            f = jfc.getSelectedFile();
            File fd = new File("imagenes/eventos", f.getName());
            try {
                BufferedInputStream bis = new BufferedInputStream(
                        new FileInputStream(f));
                BufferedOutputStream bos = new BufferedOutputStream(
                        new FileOutputStream(fd));
                int c;
                while ((c = bis.read()) != -1) {
                    bos.write(c);
                }
                bos.flush();
                bos.close();
                bis.close();
                ImageIcon imIc = new ImageIcon("imagenes/eventos/" + f.getName());
                Image imag = imIc.getImage();
                /*imag = imag.getScaledInstance(

                imIc.getIconWidth()/4, 
                imIc.getIconHeight()/4, Image.SCALE_SMOOTH);
                imag = imag.getScaledInstance(
                120, 
                120, Image.SCALE_SMOOTH);*/
                int max = Math.max(imIc.getIconWidth(), imIc.getIconHeight());
                double fe = 120.0 / max;
                imag = imag.getScaledInstance(
                        (int) (imIc.getIconWidth() * fe),
                        (int) (imIc.getIconHeight() * fe), Image.SCALE_SMOOTH);
                imIc.setImage(imag);
                lblFotoEvento.setIcon(imIc);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(NuevoEventoForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(NuevoEventoForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblFotoEventoMousePressed

