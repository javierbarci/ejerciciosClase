    private void leerFichero() {
        try {
            fr = new FileReader(fichero);
            char[] datos = new char[100];
            int i = fr.read(datos);
            while (i != -1){
                txaTexto.append(new String(datos));
                i = fr.read(datos);
            }
            /*int i = fr.read();
            while ( i != -1){
                char c = (char) i;
                txaTexto.append(String.valueOf(c));
                i = fr.read();
            }*/
            fr.close();
        } catch (FileNotFoundException ex) {
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, 
                "Se ha producido un error al leer el fichero : " +
                ex.getMessage());
        }
    }
